// M11 曲の構成(arrange でセクションを並べる)
// [サイクル数, パターン] を順に並べる。イントロ→ビルド→ドロップ。
setcpm(124 / 4)
arrange(
  [4, s("bd*4")],                                   // イントロ: キックのみ
  [4, stack(s("bd*4"), s("hh*8"))],                 // ビルド: ハット追加
  [8, stack(s("bd*4"), s("hh*8"), s("~ cp ~ cp"))], // ドロップ: フル
)._punchcard()
